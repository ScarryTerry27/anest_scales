        "- –ê–Ω–∞–ª–∏–∑ –≥–∞–∑–æ–≤–æ–≥–æ —Å–æ—Å—Ç–∞–≤–∞ –∫—Ä–æ–≤–∏\n"
        "- –ò—Å—Å–ª–µ–¥–æ–≤–∞–Ω–∏–µ —Å–Ω–∞\n"
        "- –ü—Ä–µ–¥–æ–ø–µ—Ä–∞—Ü–∏–æ–Ω–Ω—ã–π CPAP\n"
